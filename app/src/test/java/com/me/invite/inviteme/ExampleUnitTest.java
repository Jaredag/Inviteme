package com.me.invite.inviteme;

import org.junit.Before;
import org.junit.Test;

import static org.junit.Assert.*;
/**
 * Example local unit test, which will execute on the development machine (host).
 *
 * @see <a href="http://d.android.com/tools/testing">Testing documentation</a>
 */
public class ExampleUnitTest {
    @Test
    public void addition_isCorrect() throws Exception {
        assertEquals(4, 2 + 2);
    }

    @Test
    public void userConstructorShouldWork(){
        User user = new User("Michael", "100");
        String name = user.getUserName();
        String number = user.getPhoneNumber();
        assertEquals(name, "Michael");
        assertEquals(number, "100");
    }

    @Test
    public void eventConstructorShouldWork(){
        Event event = new Event("Mario Party...Party?");
        String title = event.getTitle();
        assertEquals(title, "Mario Party...Party?");
    }

    @Test
    public void eventDefaultConstructor(){
        Event event = new Event();

        assertEquals(event.getTitle(), "Enter Title");
    }


}
